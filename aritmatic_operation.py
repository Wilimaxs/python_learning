# user input
a = int(input("Value a= "))
b = int(input("Value b= "))

# sum
sum = str(a + b)
print(f"value {a} + {b} is " +sum)

# Reduction
less = str(a - b)
print(f"value {a} - {b} is "+less)

#  Multiplication
multi = str(a * b)
print(f"value {a} * {b} is "+multi)

# Division
div = str(a / b)
print(f"value {a} / {b} is "+div)

# Modular
mod = str(a % b)
print(f"value {a} % {b} is "+mod)

# rank
rank = str(a ** b)
print(f"value {a} ** {b} is "+rank)